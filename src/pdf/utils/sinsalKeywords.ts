/** 신살 position을 정렬 순서로 변환 */
const POS_ORDER: Record<string, number> = { '연주': 0, '월주': 1, '일주': 2, '시주': 3 };

/** 신살을 주별로 그룹핑하고 정렬 */
export function groupSinsalByPillar(sinsal: Array<{ name: string; position: string }>): Array<{ position: string; items: Array<{ name: string; desc: string }> }> {
  const groups: Record<string, Array<{ name: string; desc: string }>> = {};

  for (const ss of sinsal) {
    const pos = ss.position;
    if (!groups[pos]) groups[pos] = [];
    groups[pos].push({ name: ss.name, desc: SINSAL_DESC[ss.name] ?? '' });
  }

  return Object.entries(groups)
    .sort(([a], [b]) => (POS_ORDER[a] ?? 9) - (POS_ORDER[b] ?? 9))
    .map(([position, items]) => ({ position, items }));
}
