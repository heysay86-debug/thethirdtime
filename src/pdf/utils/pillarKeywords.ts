// ─── 텍스트 생성 함수 ────────────────────────────────────────

export interface PillarKeywordInput {
  position: PillarPos;
  gan: string;         // 천간 한자 (예: '丙')
  ji: string;          // 지지 한자 (예: '寅')
  ganTenGod: string;   // 천간 십성 (예: '편재')
  jiTenGod: string;    // 지지 십성 (예: '식신')
  twelveStage: string; // 십이운성 (예: '병')
}

function fillTemplate(template: string, name: string, kws: string[]): string {
  return template
    .replace('{name}', name)
    .replace('{kw1}', kws[0] ?? '')
    .replace('{kw2}', kws[1] ?? '')
    .replace('{kw3}', kws[2] ?? '')
    .replace('{kw4}', kws[3] ?? '');
}

/**
 * 십성 키를 비겁/식상/재성/관성/인성 5분류로 매핑
 */
function toSipseongCategory(tenGod: string): string {
  const key = tenGod.replace(/\(.*\)/, '').trim();
  if (['비견', '겁재'].includes(key)) return '비겁';
  if (['식신', '상관'].includes(key)) return '식상';
  if (['편재', '정재'].includes(key)) return '재성';
  if (['편관', '정관'].includes(key)) return '관성';
  if (['편인', '정인'].includes(key)) return '인성';
  return '';
}

/**
 * 단일 주(柱)의 자연어 해석 텍스트를 조립한다.
 * LLM 호출 없이 엔진 데이터 + 키워드 테이블로 결정적 생성.
 */
/**
 * 운성 총론 가져오기 (TwelveStagesSection 해설 카드용)
 */
export function getStageSummary(stage: string): string {
  return SIBII_DATA[stage]?.summary ?? '';
}

export function getStageByPillar(stage: string, position: 'year' | 'month' | 'day' | 'hour'): string {
  return SIBII_DATA[stage]?.byPillar[position] ?? '';
}

export function buildPillarReading(input: PillarKeywordInput): string {
  const { position, gan, ji, ganTenGod, jiTenGod, twelveStage } = input;
  const ctx = PILLAR_CONTEXT[position];
  const conn = SIPSEONG_CONNECTOR[position];
  const paragraphs: string[] = [];

  // 도입부
  paragraphs.push(`${ctx.domain}을(를) 담당하는 이 주는 ${ctx.period}의 환경과 성격 기반을 형성합니다.`);

  // 천간 십성 키워드
  const ganKey = ganTenGod.replace(/\(.*\)/, '').trim();
  const ganKw = SIPSEONG_KEYWORDS[ganKey]?.[position];
  if (ganKw) {
    paragraphs.push(fillTemplate(conn.gan, ganTenGod, ganKw));
  }

  // 지지 십성 키워드
  const jiKey = jiTenGod.replace(/\(.*\)/, '').trim();
  const jiKw = SIPSEONG_KEYWORDS[jiKey]?.[position];
  if (jiKw) {
    paragraphs.push(fillTemplate(conn.ji, jiTenGod, jiKw));
  }

  // 십이운성 — 주별 해석
  const stageData = SIBII_DATA[twelveStage];
  if (stageData) {
    const pillarReading = stageData.byPillar[position];
    if (pillarReading) {
      paragraphs.push(`십이운성 ${twelveStage}의 영향으로, ${pillarReading}`);
    }

    // 십이운성 × 십성 교차 해석 (천간 기준)
    const ganCat = toSipseongCategory(ganTenGod);
    const sipseongReading = ganCat ? stageData.bySipseong[ganCat] : '';
    if (sipseongReading) {
      paragraphs.push(sipseongReading);
    }
  }

  return paragraphs.join(' ');
}
