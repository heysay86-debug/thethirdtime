/** 용어 검색 (정확 매칭) */
export function findGlossaryEntry(term: string): GlossaryEntry | undefined {
  return GLOSSARY.find(e => e.term === term);
}

/** 텍스트에서 사전 키워드를 스캔하여 매칭된 항목 반환 (등장 순서) */
export function extractGlossaryTerms(text: string): GlossaryEntry[] {
  const found: GlossaryEntry[] = [];
  const seen = new Set<string>();

  for (const entry of GLOSSARY) {
    if (!seen.has(entry.term) && text.includes(entry.term)) {
      found.push(entry);
      seen.add(entry.term);
    }
  }

  return found;
}
