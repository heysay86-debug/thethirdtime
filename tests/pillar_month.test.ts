import { getMonthPillar } from '@engine/pillar_month';

describe('pillar_month — 월주 산출', () => {
  // 2024년 연간 = 甲 → 오호둔: 정월(寅) 월간 = 丙
  // 丙寅(1월) → 丁卯(2월) → 戊辰(3월) → 己巳(4월) → 庚午(5월) → 辛未(6월)
  // → 壬申(7월) → 癸酉(8월) → 甲戌(9월) → 乙亥(10월) → 丙子(11월) → 丁丑(12월)

  describe('2024년 각 월 기본 검증', () => {
    // 입춘(2/4 17:27) 이후 ~ 경칩(3/5 11:22) 이전 = 寅月
    it('2024-02-15 → 丙寅 (1월/인월)', () => {
      expect(getMonthPillar(new Date(2024, 1, 15))).toEqual({ gan: '丙', ji: '寅' });
    });

    // 경칩(3/5 11:22) 이후 ~ 청명(4/4 16:02) 이전 = 卯月
    it('2024-03-15 → 丁卯 (2월/묘월)', () => {
      expect(getMonthPillar(new Date(2024, 2, 15))).toEqual({ gan: '丁', ji: '卯' });
    });

    // 망종(6/5 13:10) 이후 ~ 소서(7/6 23:20) 이전 = 午月
    it('2024-06-15 → 庚午 (5월/오월)', () => {
      expect(getMonthPillar(new Date(2024, 5, 15))).toEqual({ gan: '庚', ji: '午' });
    });

    // 대설(12/7 00:17) 이후 ~ 소한(2025/1/5) 이전 = 子月
    it('2024-12-15 → 丙子 (11월/자월)', () => {
      expect(getMonthPillar(new Date(2024, 11, 15))).toEqual({ gan: '丙', ji: '子' });
    });
  });

  describe('절입 경계 — 2024년 경칩 3/5 11:22', () => {
    it('3/5 11:00 (경칩 22분 전) → 丙寅 (인월)', () => {
      expect(getMonthPillar(new Date(2024, 2, 5, 11, 0))).toEqual({ gan: '丙', ji: '寅' });
    });

    it('3/5 12:00 (경칩 38분 후) → 丁卯 (묘월)', () => {
      expect(getMonthPillar(new Date(2024, 2, 5, 12, 0))).toEqual({ gan: '丁', ji: '卯' });
    });
  });

  describe('야간 절입 경계 — 2024년 소서 7/6 23:20', () => {
    it('7/6 23:00 (소서 20분 전) → 庚午 (오월)', () => {
      expect(getMonthPillar(new Date(2024, 6, 6, 23, 0))).toEqual({ gan: '庚', ji: '午' });
    });

    it('7/6 23:30 (소서 10분 후) → 辛未 (미월)', () => {
      expect(getMonthPillar(new Date(2024, 6, 6, 23, 30))).toEqual({ gan: '辛', ji: '未' });
    });
  });

  // 1월 출생: 소한 이후 = 丑月, 연간은 전년도 기준
  describe('1월 출생 (소한~입춘 구간)', () => {
    // 2024년 소한: 1/6 05:49, 입춘: 2/4 17:27
    // 연간 = 2023년 癸 → 오호둔: 甲寅 기준, 丑月은 寅에서 11번째
    // 甲寅→乙卯→丙辰→己巳→...→乙丑. 계산: 甲(0)+11=11%10=1→乙, 丑
    it('2024-01-15 → 乙丑 (12월/축월, 연간=癸)', () => {
      expect(getMonthPillar(new Date(2024, 0, 15))).toEqual({ gan: '乙', ji: '丑' });
    });
  });

  // 다른 연간으로 오호둔 검증: 1986년 = 丙寅년, 연간 丙 → 정월 월간 庚
  describe('오호둔 검증 — 1986년 (연간 丙)', () => {
    // 庚寅(1월) → 辛卯(2월) → 壬辰(3월) → ...
    it('1986-09-15 → 丁酉 (8월/유월)', () => {
      // 庚(6)+7=13%10=3→丁, 酉
      expect(getMonthPillar(new Date(1986, 8, 15))).toEqual({ gan: '丁', ji: '酉' });
    });
  });
});
